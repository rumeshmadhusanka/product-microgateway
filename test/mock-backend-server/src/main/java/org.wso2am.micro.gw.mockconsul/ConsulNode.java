package org.wso2am.micro.gw.mockconsul;

/**
 * ConsulNode is ether a consul server or a client
 * Information on a ConsulNode is stored here
 */
public class ConsulNode {
    private String nodeId;
    private String nodeName;
    private String address;


    public ConsulNode(String nodeId, String nodeName, String address) {
        this.nodeId = nodeId;
        this.nodeName = nodeName;
        this.address = address;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getNodeId() {
        return nodeId;
    }

    public void setNodeId(String nodeId) {
        this.nodeId = nodeId;
    }

    public String getNodeName() {
        return nodeName;
    }

    public void setNodeName(String nodeName) {
        this.nodeName = nodeName;
    }
}
